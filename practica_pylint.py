"""
Contiene una Funcion para sumar 2 numeros
"""


def sumar(numero1, numero2):
    """
    Esta fuencion suma 2 nuemeros y retorna su valor
    nuemro1:param numero1:
    numero2:param numero2:
    :return: retorna el total
    """
    return numero1 + numero2


SUMA = sumar(5, 7)
print(f"La suma es de {SUMA}")
